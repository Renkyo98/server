function getNOindex( baseNo)
	if baseNo >= 100000 and baseNo < 100240 then
		metamo = baseNo - 100000
		for i = 1,12 do
			if metamo >=  (i-2) * 20 and metamo <  i * 20 then
				return i;
			end
		end
	elseif baseNo >= 100700 and baseNo < 100820 then
		metamo = baseNo - 100700
		for i = 1,12 do
			if metamo >=  (i-2) * 10 and metamo <  i * 10 then
				return i;
			end
		end
	elseif baseNo >= 101157 and baseNo < 101162 then
		return 2;
	elseif baseNo >= 101172 and baseNo < 101177 then
		return 4;
	elseif baseNo >= 101167 and baseNo < 101172 then
		return 7;
	elseif baseNo >= 100446 and baseNo < 100456 then
		return 8;
	elseif baseNo >= 101162 and baseNo < 101167 then
		return 8;
	elseif baseNo >= 100456 and baseNo < 100466 then
		return 11;
	elseif baseNo >= 114990 and baseNo < 115020 then
		return 13;
	elseif baseNo >= 120100 and baseNo < 120105 then
		return 13;
	elseif baseNo >= 120110 and baseNo < 120115 then
		return 13;
	elseif baseNo >= 114960 and baseNo < 114990 then
		return 14;
	elseif baseNo >= 120105 and baseNo < 120110 then
