					return 1;
				end
			end
		end

		for i = 1,table.getn(PlayerColor[1]) do
			if PlayerColor[playerid][i] == char.getInt( meindex,"BASEBASEIMAGENUMBER") or (char.getInt( meindex,"BASEBASEIMAGENUMBER") >= 100700 and char.getInt( meindex,"BASEBASEIMAGENUMBER") < 108400) then
				if petNo == CommonPetList[i] then
					return 1
				end
			end
		end

		for i = 1,table.getn(CommonPetList1) do
			if char.getInt( meindex,"BASEBASEIMAGENUMBER") == CommonPetList1[i][2] and petNo == CommonPetList1[i][3] then
				return 1;
			end
		end


		for i = 1,table.getn(Pointpetlist) do
			if petNo == Pointpetlist[i][1] then
				floorid = char.getWorkInt( meindex,"FLOOR")
				for j = 1,table.getn(Pointpetlist[i][2]) do
					if Pointpetlist[i][2][j] == floorid then
