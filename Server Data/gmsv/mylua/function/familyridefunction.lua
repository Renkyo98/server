function CheckRideNoByPetNo( meindex,petNo)
	local playerid = getNOindex(char.getInt( meindex,"BASEBASEIMAGENUMBER"));
	if playerid > 0 then
		for i = 1,table.getn(ridedata) do
			if petNo == ridedata[i][2] then
				--print(NewRideNoList[playerid][i]);
				return ridedata[i][3][playerid];
			end
		end

		for i = 1,table.getn(PlayerColor[1]) do
			if PlayerColor[playerid][i] == char.getInt( meindex,"BASEBASEIMAGENUMBER") or (char.getInt( meindex,"BASEBASEIMAGENUMBER") >= 100700 and char.getInt( meindex,"BASEBASEIMAGENUMBER") < 108400) then
				if petNo == CommonPetList[i] then
					--print(CommonRideNoList[playerid][i]);
					return CommonRideNoList[playerid][i];
				end
			end
		end

		for i = 1,table.getn(CommonPetList1) do
			if char.getInt( meindex,"BASEBASEIMAGENUMBER") == CommonPetList1[i][2] and petNo == CommonPetList1[i][3] then
				--print(CommonPetList1[i][1]);
				return CommonPetList1[i][1];
			end
		end


		for i = 1,table.getn(Pointpetlist) do
			if petNo == Pointpetlist[i][1] then
				floorid = char.getWorkInt( meindex,"FLOOR")
				for j = 1,table.getn(Pointpetlist[i][2]) do
					if Pointpetlist[i][2][j] == floorid then
						if char.getInt( meindex,"FMLEADERFLAG") > 0 and char.getInt( meindex,"FMLEADERFLAG") ~= 2 then
							if char.getInt( meindex,"소痢잚謹") == Pointpetlist[i][3] then
								if PointRideNoList[playerid][i] > -1 then
									--print(PointRideNoList[playerid][i]);
									return PointRideNoList[playerid][i];
								end
							end
						end
					end
				end
			end
		end
	end
	return -1;
end

function CheckPetRideByPetNo( meindex,petNo)
	local playerid = getNOindex(char.getInt( meindex,"BASEBASEIMAGENUMBER"))

	if char.getInt( meindex,"BASEIMAGENUMBER") == 101178 then
		if petNo == 101177 then
			return 101179;
		end
	elseif char.getInt( meindex,"BASEIMAGENUMBER") == 101177 then
		if petNo == 101178 then
			return 101179;
		end
	elseif playerid > 0 then
		for i = 1,table.getn(ridedata) do
			if petNo == ridedata[i][2] then
				if other.DataAndData(char.getInt(meindex,"LOWRIDEPETS"),i - 1) ~= 0 then
					return 1;
				end
